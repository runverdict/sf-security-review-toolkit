#!/usr/bin/env node
/*
 * inject-report-headline.mjs — DETERMINISTIC injection of the mandated cluster-headline
 * block into the audit report, so the block's PRESENCE never depends on the synthesis LLM
 * remembering to paste it.
 *
 * WHY THIS EXISTS. audit-codebase Step 6 mandates the report's executive summary be HEADED
 * by the verbatim finding-cluster headline. `merge-ledger.mjs` already EMITS that block to
 * `report-headline.md` (and `render-recap.mjs` refreshes it POST-disposition), which removed
 * the "recompute the right, post-disposition block" burden — but the SKILL still asked the
 * agent to INCLUDE that file, and a cold run's synthesis agent SKIPPED it, tripping the
 * `verify-report-headline.mjs` hard stop and needing a hand-rescue. This closes the last gap:
 * a deterministic step splices the current-ledger block into the report at Step 7 (after the
 * post-disposition re-render, before verify), so the block is present BY CONSTRUCTION. The
 * agent writes prose; the engine owns the block. `verify-report-headline.mjs` stays the
 * backstop (it still catches a contradicting claim in the agent's surrounding prose).
 *
 * The block is recomputed from the CURRENT ledger via the SAME symbols every other surface
 * uses (`renderClusterHeadline` + `clusterOrNullFromFindings`, imported — never reimplemented),
 * so the injected block and the verify gate's `expected` can never drift.
 *
 * PLACEMENT (deterministic, idempotent):
 *   1. an already-injected region (between the SRT markers) → replaced in place;
 *   2. a bare `<!-- SRT:CLUSTER-HEADLINE ... -->` placeholder the agent left → replaced;
 *   3. no marker → inserted right AFTER the first heading (the executive-summary heading);
 *   4. no heading at all → prepended.
 * Running twice over the same ledger is a byte no-op.
 *
 * PURE core (`injectHeadline`) + thin CLI. Writes ONLY the report file. FAILS CLOSED (exit 2)
 * when the ledger is unreadable (a report must never ship a headline we could not compute) or
 * the report is missing. No LLM, no network, no deps.
 *
 * USAGE: node inject-report-headline.mjs --target <repo> --report <path> [--json]
 */
import { readFileSync, writeFileSync, existsSync, realpathSync } from 'node:fs'
import { join, isAbsolute } from 'node:path'
import { fileURLToPath } from 'node:url'
import { renderClusterHeadline, clusterOrNullFromFindings } from './finding-clusters.mjs'

export const HEADLINE_START = '<!-- SRT:CLUSTER-HEADLINE (deterministic — regenerated by inject-report-headline.mjs; do not hand-edit) -->'
export const HEADLINE_END = '<!-- /SRT:CLUSTER-HEADLINE -->'
// Any short placeholder the agent may leave in the exec summary (the START marker itself is the
// long descriptive form; this matches a bare `<!-- SRT:CLUSTER-HEADLINE -->` and never the END
// marker, whose `/SRT` defeats the `\s*SRT` anchor).
const PLACEHOLDER_RE = /<!--\s*SRT:CLUSTER-HEADLINE\b[^>]*-->/

/**
 * PURE. Return `reportText` with the cluster-headline `block` deterministically present,
 * wrapped in the SRT markers. Idempotent: re-injecting the same block is a byte no-op. Never
 * throws; `\r\n` is normalized to `\n` (the block itself is `\n`-only).
 */
export function injectHeadline(reportText, block) {
  const report = String(reportText ?? '').replace(/\r\n/g, '\n')
  const region = `${HEADLINE_START}\n\n${String(block ?? '')}\n\n${HEADLINE_END}`

  // 1. a prior injected region (full START marker present) → replace START..END in place.
  const startIdx = report.indexOf(HEADLINE_START)
  if (startIdx >= 0) {
    const endIdx = report.indexOf(HEADLINE_END, startIdx + HEADLINE_START.length)
    if (endIdx >= 0) return report.slice(0, startIdx) + region + report.slice(endIdx + HEADLINE_END.length)
    // dangling START, no END — replace only the START marker with the full region.
    return report.slice(0, startIdx) + region + report.slice(startIdx + HEADLINE_START.length)
  }

  // 2. a bare agent-left placeholder → replace it with the region.
  if (PLACEHOLDER_RE.test(report)) return report.replace(PLACEHOLDER_RE, region)

  // 3. no marker → insert after the FIRST heading (the exec-summary heading).
  const lines = report.split('\n')
  const hIdx = lines.findIndex((l) => /^#{1,6}\s/.test(l))
  if (hIdx >= 0) {
    lines.splice(hIdx + 1, 0, '', region)
    return lines.join('\n')
  }

  // 4. no heading at all → prepend.
  return report.trim() ? `${region}\n\n${report}` : `${region}\n`
}

function main() {
  const arg = (flag, def) => {
    const i = process.argv.indexOf(flag)
    return i >= 0 && process.argv[i + 1] ? process.argv[i + 1] : def
  }
  const TARGET = arg('--target', process.cwd())
  const REPORT = arg('--report', null)
  const AS_JSON = process.argv.includes('--json')
  if (!REPORT) { console.error('inject-report-headline: --report <path> is required'); process.exit(2) }
  const reportPath = isAbsolute(REPORT) || existsSync(REPORT) ? REPORT : join(TARGET, REPORT)

  let ledger
  try { ledger = JSON.parse(readFileSync(join(TARGET, '.security-review', 'audit-ledger.json'), 'utf8')) } catch { ledger = null }
  if (!ledger || typeof ledger !== 'object' || Array.isArray(ledger)) {
    console.error(`inject-report-headline: cannot read the audit ledger under ${TARGET} — refusing to inject a headline we cannot compute (fail closed)`)
    process.exit(2)
  }
  const cluster = clusterOrNullFromFindings(ledger.findings)
  if (!cluster) {
    console.error('inject-report-headline: ledger `findings` is not an array (corrupted/hand-edited) — cannot compute the block; failing closed')
    process.exit(2)
  }
  const block = renderClusterHeadline(cluster)

  let report
  try { report = readFileSync(reportPath, 'utf8') } catch {
    console.error(`inject-report-headline: cannot read the report at ${reportPath} — write the report first, then inject`)
    process.exit(2)
  }
  const injected = injectHeadline(report, block)
  const changed = injected !== report.replace(/\r\n/g, '\n')
  if (changed) writeFileSync(reportPath, injected)
  const msg = `inject-report-headline: ${changed ? 'injected' : 'already current'} — the verbatim cluster headline is present in ${reportPath} (recomputed from the ledger; verify-report-headline is the backstop).`
  if (AS_JSON) process.stdout.write(JSON.stringify({ ok: true, changed, reportPath }, null, 2) + '\n')
  else process.stdout.write(msg + '\n')
}

function invokedDirectly() {
  if (!process.argv[1]) return false
  try { return realpathSync(fileURLToPath(import.meta.url)) === realpathSync(process.argv[1]) }
  catch { return fileURLToPath(import.meta.url) === process.argv[1] }
}
if (invokedDirectly()) main()
